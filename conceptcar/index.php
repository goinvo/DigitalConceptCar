				<!-- STICKINESS OF DECISIONS  -->
				<div class="stickiness">
					<div class="optimal">
						<h2><span>Stickiness of Decisions &#8211; Optimal</h2>
						<p>
							Decisions made&#8212;whether they are aesthetic, behavioral, structural or with regard to
							branding&#8212;are so well informed they are likely to be valid for the final shipping product.
						</p>
					</div>
					<div class="effective">
						<h2><span>Stickiness of Decisions &#8211; Effective</h2>
						<p>
							It's possible to make critical design and engineering decisions, but there will be a portion, often
							with regard to behavioral details, that cannot be made final until feedback is gathered from the real, working product.
						</p>
					</div>
					<div class="acceptable">
						<h2><span>Stickiness of Decisions &#8211; Acceptable</h2>
						<p>
							Most, if not all, design and engineering decisions made at this level will require some sort of
							followup, verification, or validation when the real product is built.
						</p>
					</div>
					<div class="marginal">
						<h2><span>Stickiness of Decisions &#8211; Minimal</h2>
						<p>
							Only the most common and basic decisions can be made. These are generally decisions
							about product direction overall, and can be compared to the same kinds of decisions one would make
							with pixel-perfect screen renderings.
						</p>
					</div>
					<div class="busywork">
						<h2><span>Stickiness of Decisions &#8211; Marginal</h2>
						<p>
							Decisions based on this prototype are rarely useful for anyone outside of the design team. Using only
							this type of prototype often requires designers to create low-fidelity assets that wind up filling
							time rather than contributing tangible progress towards shipping a product.
						</p>
					</div>
				</div>

				<!-- ABILITY TO ITERATE  -->
				<div class="speed">
					<div class="optimal">
						<h2><span>Overall Speed &#8211; Optimal</h2>
						<p>
							Significant time savings will be seen over the entire course of the project, anywhere from
							33% to 50% versus other prototyping methods.
						</p>
					</div>
					<div class="effective">
						<h2><span>Overall Speed &#8211; Effective</h2>
						<p>
							Respectable time savings will be seen over the course of the project, anywhere from 20% to
							33% versus other prototyping methods.
						</p>
					</div>
					<div class="acceptable">
						<h2><span>Overall Speed  &#8211; Acceptable</h2>
						<p>
							Decent time savings will be seen over the course of the project, anywhere from 10% to 20%
							versus other prototyping methods.
						</p>
					</div>
					<div class="marginal">
						<h2><span>Overall Speed &#8211; Minimal</h2>
						<p>
							Little to no time savings will be seen over the course of the project versus other prototyping methods.
						</p>
					</div>
					<div class="busywork">
						<h2><span>Overall Speed &#8211; Marginal</h2>
						<p>
							While at first it may feel like time is being saved, at best, no time is saved over the overall
							course of the project, and at worst, time is added due to the inappropriateness or inadequacy
							of the prototype for the product category.
						</p>
					</div>
				</div>



				<!-- ABILITY TO ITERATE  -->
				<div class="iterate">
					<div class="optimal">
						<h2><span>Ability to Iterate &#8211; Optimal</h2>
						<p>
							The nature of the prototype, once built, provides ample opportunity to experiment with a wide
							variety of design choices with very little reconstruction time. More often than not, changing
							the prototype will be faster than changing any other static deliverable.
						</p>
					</div>
					<div class="effective">
						<h2><span>Ability to Iterate &#8211; Effective</h2>
						<p>
							While the prototype is malleable if constructed smartly, it will require some time to make changes. 
							These changes generally take the same amount of time as reconstructing pixel-perfect screen renderings.
						</p>
					</div>
					<div class="acceptable">
						<h2><span>Ability to Iterate &#8211; Acceptable</h2>
						<p>
							Changing the prototype is often slower than fixing static deliverables, however, the time difference
							is not significant enough to lose the ability to see the changes in action.
						</p>
					</div>
					<div class="marginal">
						<h2><span>Ability to Iterate  &#8211; Minimal</h2>
						<p>
							The speed to change the prototype is such that it would only be done at key stages, and as such, 
							may impede constant iteration of the overall design direction.
						</p>
					</div>
					<div class="busywork">
						<h2><span>Ability to Iterate  &#8211; Marginal</h2>
						<p>
							Time would be better spent iterating with static deliverables than spent iterating this type of prototype.
						</p>
					</div>
				</div>



				<!-- REUSE OF ASSETS  -->
				<div class="assets">
					<div class="optimal">
						<h2><span>Reuse of Assets &#8211; Optimal</h2>
						<p>
							More than 80% of the assets made for the prototype can literally be copied and pasted into a
							source control system to be used for the final shipping product.
						</p>
					</div>
					<div class="effective">
						<h2><span>Reuse of Assets &#8211; Effective</h2>
						<p>
							Roughly 50% to 75% of the assets made for the prototype can be used in the final shipping product.
						</p>
					</div>
					<div class="acceptable">
						<h2><span>Reuse of Assets &#8211; Acceptable</h2>
						<p>
							Approximately 25% to 50% of the assets made for the prototype can be used in the final shipping 
							product.
						</p>
					</div>
					<div class="marginal">
						<h2><span>Reuse of Assets &#8211; Minimal</h2>
						<p>
							Few, if any, of the assets made for the prototype can be used in the final shipping product.
						</p>
					</div>
					<div class="busywork">
						<h2><span>Reuse of Assets &#8211; Marginal</h2>
						<p>
							There is nothing that you can repurpose here. All assets made at this level are purely for the
							purpose of internal design and engineering processes, and are of little to no use outside of the team.
						</p>
					</div>
				</div>



				<div class="code">
					<div class="optimal">
						<h2><span>Reuse of Code &#8211; Optimal</h2>
						<p>
							Most of the code and scripts written for the prototype can be used as is for the final shipping
							product.
						</p>
					</div>
					<div class="effective">
						<h2><span>Reuse of Code &#8211; Effective</h2>
						<p>
							A significant portion of the code and scripts written for the prototype can be used in the final shipping product.
							Often times, code will have to be refactored into a different form by engineering. For example,
							HTML layout code might be refactored into Java code that writes out the HTML layout.
						</p>
					</div>
					<div class="acceptable">
						<h2><span>Reuse of Code &#8211; Acceptable</h2>
						<p>
							Code and scripts written are good enough for prototyping, but will always have to be refactored
							into a final form by engineering.
						</p>
					</div>
					<div class="marginal">
						<h2><span>Reuse of Code &#8211; Minimal</h2>
						<p>
							Code and scripts written are only useful for the purpose of prototyping.
						</p>
					</div>
					<div class="busywork">
						<h2><span>Reuse of Code &#8211; Marginal</h2>
						<p>
							There is nothing that you can repurpose here, not even for future prototypes. Any code written
							 is a complete one-off, making it useful for only that prototype.
						</p>
					</div>
				</div>



				<div class="stakeholders">
					<div class="optimal">
						<h2><span>Stakeholder Involvement &#8211; Optimal</h2>
						<p>
							You can safely have executives, customers and sales people view and interact with the prototype
							without specific guidance. Feedback received from these stakeholders about this prototype will be as
							good as feedback received about final, shipping products.
						</p>
					</div>
					<div class="effective">
						<h2><span>Stakeholder Involvement &#8211; Effective</h2>
						<p>
							You can safely have executives, customers and sales people view and interact with the prototype
							as long as they are guided through it or someone with knowledge drives it. Feedback received
							on this type of prototype from stakeholders is often very good.
						</p>
					</div>
					<div class="acceptable">
						<h2><span>Stakeholder Involvement &#8211; Acceptable</h2>
						<p>
							You can have executives and customers view the prototype as long as someone with knowledge of the
							prototype guides or walks them through it.
						</p>
					</div>
					<div class="marginal">
						<h2><span>Stakeholder Involvement &#8211; Minimal</h2>
						<p>
							Sharing this type of prototype is usually only meaningful to people who have working knowledge of the project and understand
							that what they are seeing is incomplete. Feedback here may be useful for the design and development team, but is mostly inadequate
							to make critical decisions without supplemental data.
						</p>
					</div>
					<div class="busywork">
						<h2><span>Stakeholder Involvement &#8211; Marginal</h2>
						<p>
							Involving stakeholders in reviewing this type of prototype is usually not valuable unless they are deeply involved in
							the project and have an understanding of the design process up to that point.
						</p>
					</div>
				</div>



